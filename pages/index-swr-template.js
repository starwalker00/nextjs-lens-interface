function HomeSwr() {
  const address = `https://randomuser.me/api/?results=6`;
  const fetcher = async (url) => await axios.get(url).then((res) => res.data);
  const { data, error } = useSWR(address, fetcher);
  if (error) <p>Loading failed...</p>;
  if (!data) <h1>Loading...</h1>;
  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <ChakraProvider theme={theme}>
        <CSSReset />
        <Box w={800} p={4} m="20px auto">
          <Heading as="h1" size="xl" textAlign="center" m={5}>
            Chakra Example
          </Heading>
          <Box as="p" textAlign="center">
            {/* {data && <Box>{JSON.stringify(data.results)}</Box>} */}
            {data && <FactListSwrTemplate results={data.results} ></FactListSwrTemplate>}
          </Box>
        </Box>
      </ChakraProvider>
    </>
  )
}

